What the research shows: 
* total gluconeogenesis (gn) contributes about 50% to glucose levels and the other 50% comes from glycogenelysis
* this means: total glucose produced by gn = 4.65mmol/l / 2 = 2.235mmol/l
* for 8-24 hours:
      GIT = 5-7% of gn, will use 6%
          = 1.395mmol/dl
* for +24 hours:
      GIT = 20-25% of gn, will use 22.5%
          = 0.503mmol/dl
